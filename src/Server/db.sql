USE MASTER;
DROP DATABASE IF EXISTS BEEKEY;

CREATE DATABASE BEEKEY;
USE BEEKEY;

CREATE TABLE ACCOUNT(
	USERNAME VARCHAR(30),
	PASSWORD CHAR(64),
	TYPE INT,
	GENDER BOOLEAN,
	PHONE VARCHAR(12),
	ADDRESS VARCHAR(30),
	COIN INT,
	
	PRIMARY KEY(USERNAME)
);

CREATE TABLE CATEGORY(
	CATEGORY_ID INT AUTO_INCREMENT,
	CATEGORY_NAME VARCHAR(30),

	PRIMARY KEY(CATEGORY_ID)
);

CREATE TABLE TASK(
	TASK_ID INT AUTO_INCREMENT,
	DESCRIPTION TEXT,
	OFFER INT,
	DEADLINE DATETIME,
	CATEGORY_ID INT,
	LANCER_ID VARCHAR(30),
	USER_ID VARCHAR(30),

	PRIMARY KEY(TASK_ID)
);

CREATE TABLE FEEDBACK(
	FEEDBACK_ID INT AUTO_INCREMENT,
	DESCRIPTION TEXT,
	STAR INT,
	LANCER_ID VARCHAR(30),
	USER_ID VARCHAR(30),
	
	PRIMARY KEY(FEEDBACK_ID)
);

CREATE TABLE SOLUTION(
	SOLUTION_ID INT AUTO_INCREMENT,
	DESCRIPTION TEXT,
	STATUS BOOLEAN,
	UPLOAD_DATE DATETIME,
	LANCER_ID VARCHAR(30),
	TASK_ID INT,

	PRIMARY KEY(SOLUTION_ID)
);

CREATE TABLE COUNTER_OFFER(
	TASK_ID INT,
	LANCER_ID VARCHAR(30),
	OFFER INT,
	REASON TEXT,

	PRIMARY KEY(TASK_ID,LANCER_ID)
);

CREATE TABLE CHAT(
	SEND_ID VARCHAR(30),
	RECEIVE_ID VARCHAR(30),
	DATE_TIME DATETIME,
	CONTENT TEXT,

	PRIMARY KEY(SEND_ID,RECEIVE_ID,DATE_TIME)
);

CREATE TABLE ATTACH_FILE(
	TASK_ID INT,
	FILE_PATH VARCHAR(500),

	PRIMARY KEY(TASK_ID,FILE_PATH)
);

